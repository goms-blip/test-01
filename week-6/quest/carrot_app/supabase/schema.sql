-- ────────────────────────────────────────────────────────────
-- 1) carrot_profiles  — auth.users 1:1
-- ────────────────────────────────────────────────────────────
create table if not exists public.carrot_profiles (
  id          uuid primary key references auth.users(id) on delete cascade,
  email       text not null,
  nickname    text not null,
  region      text not null default '',
  avatar_url  text,
  created_at  timestamptz not null default now()
);

alter table public.carrot_profiles enable row level security;

drop policy if exists "carrot_profiles_select_all"   on public.carrot_profiles;
drop policy if exists "carrot_profiles_insert_self"  on public.carrot_profiles;
drop policy if exists "carrot_profiles_update_self"  on public.carrot_profiles;

create policy "carrot_profiles_select_all"
  on public.carrot_profiles for select using (true);

create policy "carrot_profiles_insert_self"
  on public.carrot_profiles for insert with check (auth.uid() = id);

create policy "carrot_profiles_update_self"
  on public.carrot_profiles for update using (auth.uid() = id);

-- 회원가입 시 자동으로 carrot_profiles row 생성
create or replace function public.handle_new_carrot_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.carrot_profiles (id, email, nickname, region)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data->>'nickname', split_part(coalesce(new.email,''), '@', 1)),
    coalesce(new.raw_user_meta_data->>'region', '')
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created_carrot on auth.users;
create trigger on_auth_user_created_carrot
  after insert on auth.users
  for each row execute function public.handle_new_carrot_user();

-- ────────────────────────────────────────────────────────────
-- 2) carrot_products
-- ────────────────────────────────────────────────────────────
create table if not exists public.carrot_products (
  id           bigint generated by default as identity primary key,
  seller_id    uuid not null references auth.users(id) on delete cascade,
  title        text not null,
  price        bigint not null default 0,
  description  text not null default '',
  category     text not null default '기타',
  status       text not null default 'on_sale'
                 check (status in ('on_sale','reserved','sold')),
  region       text not null default '',
  images       text[] not null default '{}',
  created_at   timestamptz not null default now()
);

create index if not exists idx_carrot_products_created on public.carrot_products(created_at desc);
create index if not exists idx_carrot_products_region  on public.carrot_products(region);
create index if not exists idx_carrot_products_seller  on public.carrot_products(seller_id);
create index if not exists idx_carrot_products_status  on public.carrot_products(status);

alter table public.carrot_products enable row level security;

drop policy if exists "carrot_products_select_all"     on public.carrot_products;
drop policy if exists "carrot_products_insert_seller"  on public.carrot_products;
drop policy if exists "carrot_products_update_seller"  on public.carrot_products;
drop policy if exists "carrot_products_delete_seller"  on public.carrot_products;

create policy "carrot_products_select_all"
  on public.carrot_products for select using (true);

create policy "carrot_products_insert_seller"
  on public.carrot_products for insert with check (auth.uid() = seller_id);

create policy "carrot_products_update_seller"
  on public.carrot_products for update using (auth.uid() = seller_id);

create policy "carrot_products_delete_seller"
  on public.carrot_products for delete using (auth.uid() = seller_id);

