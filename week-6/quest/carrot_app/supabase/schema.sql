-- ────────────────────────────────────────────────────────────
-- 4) carrot_chat_rooms  (1:1 buyer ↔ seller)
-- ────────────────────────────────────────────────────────────
create table if not exists public.carrot_chat_rooms (
  id          bigint generated by default as identity primary key,
  product_id  bigint not null references public.carrot_products(id) on delete cascade,
  buyer_id    uuid not null references auth.users(id) on delete cascade,
  seller_id   uuid not null references auth.users(id) on delete cascade,
  created_at  timestamptz not null default now(),
  unique (product_id, buyer_id),
  check (buyer_id <> seller_id)
);

create index if not exists idx_carrot_chat_rooms_buyer  on public.carrot_chat_rooms(buyer_id);
create index if not exists idx_carrot_chat_rooms_seller on public.carrot_chat_rooms(seller_id);

alter table public.carrot_chat_rooms enable row level security;

drop policy if exists "carrot_chat_rooms_select_member" on public.carrot_chat_rooms;
drop policy if exists "carrot_chat_rooms_insert_buyer"  on public.carrot_chat_rooms;

create policy "carrot_chat_rooms_select_member"
  on public.carrot_chat_rooms for select
  using (auth.uid() = buyer_id or auth.uid() = seller_id);

create policy "carrot_chat_rooms_insert_buyer"
  on public.carrot_chat_rooms for insert
  with check (
    auth.uid() = buyer_id
    and exists (
      select 1 from public.carrot_products p
      where p.id = product_id and p.seller_id = carrot_chat_rooms.seller_id
    )
  );

-- ────────────────────────────────────────────────────────────
-- 5) carrot_chat_messages
-- ────────────────────────────────────────────────────────────
create table if not exists public.carrot_chat_messages (
  id          bigint generated by default as identity primary key,
  room_id     bigint not null references public.carrot_chat_rooms(id) on delete cascade,
  sender_id   uuid not null references auth.users(id) on delete cascade,
  text        text not null check (length(text) between 1 and 2000),
  created_at  timestamptz not null default now()
);

create index if not exists idx_carrot_chat_messages_room on public.carrot_chat_messages(room_id, id);

alter table public.carrot_chat_messages enable row level security;

drop policy if exists "carrot_chat_messages_select_member" on public.carrot_chat_messages;
drop policy if exists "carrot_chat_messages_insert_member" on public.carrot_chat_messages;

create policy "carrot_chat_messages_select_member"
  on public.carrot_chat_messages for select
  using (
    exists (
      select 1 from public.carrot_chat_rooms r
      where r.id = room_id
        and (r.buyer_id = auth.uid() or r.seller_id = auth.uid())
    )
  );

create policy "carrot_chat_messages_insert_member"
  on public.carrot_chat_messages for insert
  with check (
    auth.uid() = sender_id
    and exists (
      select 1 from public.carrot_chat_rooms r
      where r.id = room_id
        and (r.buyer_id = auth.uid() or r.seller_id = auth.uid())
    )
  );

-- ────────────────────────────────────────────────────────────
-- 6) Realtime 활성화 — chat_messages 변경을 클라이언트가 구독
-- ────────────────────────────────────────────────────────────
do $$
begin
  -- 이미 추가되어 있으면 에러나므로 try/catch
  begin
    alter publication supabase_realtime add table public.carrot_chat_messages;
