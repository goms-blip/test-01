-- ────────────────────────────────────────────────────────────
-- 7) Storage — carrot-products 버킷 + 정책
--    (대시보드 Storage 페이지에서 만들거나 아래 SQL로 한번에)
-- ────────────────────────────────────────────────────────────
insert into storage.buckets (id, name, public)
values ('carrot-products', 'carrot-products', true)
on conflict (id) do nothing;

drop policy if exists "carrot_storage_public_read"   on storage.objects;
drop policy if exists "carrot_storage_auth_insert"   on storage.objects;
drop policy if exists "carrot_storage_owner_delete"  on storage.objects;

create policy "carrot_storage_public_read"
  on storage.objects for select to public
  using (bucket_id = 'carrot-products');

-- 업로드는 본인 폴더(`{auth.uid()}/...`)에만 허용
create policy "carrot_storage_auth_insert"
  on storage.objects for insert to authenticated
  with check (
    bucket_id = 'carrot-products'
    and (storage.foldername(name))[1] = auth.uid()::text
  );

create policy "carrot_storage_owner_delete"
  on storage.objects for delete to authenticated
  using (bucket_id = 'carrot-products' and owner = auth.uid());

-- ────────────────────────────────────────────────────────────
-- 검증
-- ────────────────────────────────────────────────────────────
select t.tablename,
       case when t.rowsecurity then 'ON' else 'OFF' end as rls,
       (select count(*) from pg_policies p
          where p.schemaname='public' and p.tablename=t.tablename) as policies
from pg_tables t
where t.schemaname = 'public'
  and t.tablename like 'carrot_%'
order by t.tablename;
